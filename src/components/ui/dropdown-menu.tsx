function DropdownMenuSub({
  ...props
}: React.ComponentProps<typeof DropdownMenuPrimitive.Sub>) {
  return <DropdownMenuPrimitive.Sub data-slot="dropdown-menu-sub" {...props} />;
}

function DropdownMenuSubTrigger({
  className,
  inset,
  icon,
  children,
  ...props
}: React.ComponentProps<typeof DropdownMenuPrimitive.SubTrigger> & {
  inset?: boolean;
  icon?: string;
}) {
  return (
    <DropdownMenuPrimitive.SubTrigger
      data-slot="dropdown-menu-sub-trigger"
      data-inset={inset}
      className={cn("dropdown-subtrigger ", className)}
      {...props}
    >
      {icon && (
        <span
          className={`shrink-0 w-5 h-5 me-2.5 text-current text-xl self-start p-0 relative ${icon}`}
        ></span>
      )}
      {children}
      <span className="shrink-0 flex items-center ml-auto my-auto text-secondary text-sm self-start p-0 relative tgico tgico-chevron-right"></span>
    </DropdownMenuPrimitive.SubTrigger>
  );
}

